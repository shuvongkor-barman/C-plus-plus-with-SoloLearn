#include <iostream>
using namespace std;

int main()
{
// New Line
// endl manipulator

    cout << "Hello World!" <<endl;

    cout << "I Love Programming !" << endl;

    // Alternative New Line
    // \n The backslash (\) is called an escape character, and indicates a "special" character.

    cout << "Hello World! \n";

    cout << "I Love Programming !";

    // Two newline characters placed together result in a blank line.
    cout << "\n \n";

    // Multiple New Lines
    cout << " Hello \n world! \n I \n love \n programming!";





    return 0;
}
